#include "minishell.h"

char	*ft_strchr(const char *s, int c)
{
	char	*str;
	int		i;

	str = (char *) s;
	i = 0;
	while (str[i] != (unsigned char) c)
	{
		if (str[i] == '\0')
			return (NULL);
		i++;
	}
	return (str + i);
}

int	ft_strisonly(const char *s, char c)
{
	while (*s)
		if (*s++ != c)
			return (0);
	return (1);
}
